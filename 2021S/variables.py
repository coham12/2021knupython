# 변수 이름 짓는 규칙
# 1. 영문자 대소문자구분가능, 숫자, 언더바 한글사용가능
# ex age, Age, age3등등
#2, 첫잘이ㅔ는 숫자를 사용할수 없음
#3. 파이썬에서 자체적으로 사용하는 단어 사용 불가
#ex True, if, While 등등


# Convention
# 규칙들: 1. 한단어로 된 변수는 소문자로 적는다
#ex age, good, name 등등 
#2.  두 단어 이상의 변수는 언더바로 구분한다
#ex my_name, my_friend_name

#3. 예악어와 충돌하는 경우에는 뒤에 언더바를 붙인다.
#ex if_, time_
#4. 상수의 경우 대문자와 언더바를 혼용하여 쓴다.
#ex TOTAL, MAX_NUM, AVG 등등